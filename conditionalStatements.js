// Conditional statements
// If/else vs Switch vs Ternary Operators

const condition = 'Raining'

// If else blocks
if (condition) {
    console.log(condition)
} else {
    console.log('No condition')
}

// Switch statements
switch (condition) {
    case true:
        console.log(condition)
        break;
    default:
        console.log('No condition')
}

// Ternary Operators
condition ? console.log(condition) : console.log('No condition')